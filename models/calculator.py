class Calculator():

    def __init__(self,num1,num2):
        self.num1=num1
        self.num2=num2

    def addition(num1,num2):
        return str(int(num1) + int(num2))

    def subtraction(num1,num2):
        return str(int(num1) - int(num2))

    def multiplication(num1,num2):
        return str(int(num1) * int(num2))

    def division(num1,num2):
        return str(int(num1) / int(num2))